#!/usr/bin/env node

const fs = require("node:fs");
const path = require("node:path");

const repoRoot = path.resolve(__dirname, "..");
const outputPath = path.join(
  repoRoot,
  "src",
  "locale",
  "generatedLocaleSymbols.ts",
);

const LOCALES = [
  "en-US",
  "tr-TR",
  "en-IN",
  "de-DE",
  "fr-FR",
  "es-ES",
  "it-IT",
  "pt-BR",
  "nl-NL",
  "ru-RU",
  "ar-EG",
  "fa-IR",
  "hi-IN",
  "ja-JP",
  "ko-KR",
  "zh-CN",
  "id-ID",
];

function main() {
  const check = process.argv.includes("--check");
  const output = renderGeneratedLocaleSymbols(LOCALES);

  if (check) {
    const current = fs.existsSync(outputPath)
      ? fs.readFileSync(outputPath, "utf8")
      : "";

    if (current !== output) {
      console.error(
        "Generated locale symbols are stale. Run `npm run generate:cldr-lite`.",
      );
      process.exit(1);
    }

    return;
  }

  fs.writeFileSync(outputPath, output);
}

function renderGeneratedLocaleSymbols(locales) {
  const entries = locales
    .map((locale) => [locale, getLocaleSymbols(locale)])
    .map(([locale, symbols]) => renderLocaleEntry(locale, symbols))
    .join(",\n");

  return `/* eslint-disable */\n// Generated by scripts/generate-cldr-lite.js. Do not edit by hand.\n\nimport type { LocaleSymbols } from "./localeRegistry";\n\nexport const generatedLocaleSymbols: Record<string, LocaleSymbols> = Object.freeze({\n${entries},\n});\n`;
}

function renderLocaleEntry(locale, symbols) {
  return `  ${JSON.stringify(locale)}: Object.freeze({\n    locale: ${JSON.stringify(locale)},\n    decimal: ${JSON.stringify(symbols.decimal)},\n    group: ${JSON.stringify(symbols.group)},\n    plusSign: ${JSON.stringify(symbols.plusSign)},\n    minusSign: ${JSON.stringify(symbols.minusSign)},\n    percentSign: ${JSON.stringify(symbols.percentSign)},\n    percentPattern: Object.freeze(${renderObject(symbols.percentPattern)}),\n    currencyPattern: Object.freeze(${renderObject(symbols.currencyPattern)}),\n    grouping: Object.freeze(${renderObject(symbols.grouping)}),\n    compactPatterns: Object.freeze(${renderCompactPatternSet(symbols.compactPatterns)}),\n  })`;
}

function renderObject(value) {
  const fields = Object.entries(value)
    .filter(([, fieldValue]) => fieldValue !== undefined)
    .map(([key, fieldValue]) => `${key}: ${JSON.stringify(fieldValue)}`)
    .join(", ");

  return `{ ${fields} }`;
}

function renderCompactPatternSet(patterns) {
  return `{ short: ${renderCompactPatternArray(
    patterns.short,
  )}, long: ${renderCompactPatternArray(patterns.long)} }`;
}

function renderCompactPatternArray(patterns) {
  return `[${patterns.map(renderCompactPattern).join(", ")}]`;
}

function renderCompactPattern(pattern) {
  const one =
    pattern.one === undefined
      ? ""
      : `, one: ${renderCompactAffix(pattern.one)}`;

  return `{ thresholdPower: ${pattern.thresholdPower}, divisorPower: ${pattern.divisorPower}${one}, other: ${renderObject(
    renderCompactAffixObject(pattern.other),
  )} }`;
}

function renderCompactAffix(affix) {
  return renderObject(renderCompactAffixObject(affix));
}

function renderCompactAffixObject(affix) {
  return {
    prefix: affix.prefix.length > 0 ? affix.prefix : undefined,
    suffix: affix.suffix.length > 0 ? affix.suffix : undefined,
  };
}

function getLocaleSymbols(locale) {
  const numberParts = new Intl.NumberFormat(locale).formatToParts(12345.6);
  const plusParts = new Intl.NumberFormat(locale, {
    signDisplay: "always",
  }).formatToParts(1);
  const minusParts = new Intl.NumberFormat(locale).formatToParts(-1);
  const percentParts = new Intl.NumberFormat(locale, {
    style: "percent",
  }).formatToParts(0.1);
  const currencyParts = new Intl.NumberFormat(locale, {
    currency: "USD",
    currencyDisplay: "symbol",
    style: "currency",
  }).formatToParts(1234.5);
  const grouping = inferGrouping(locale);

  return {
    decimal: getPart(numberParts, "decimal", "."),
    group: getPart(numberParts, "group", ","),
    plusSign: getPart(plusParts, "plusSign", "+"),
    minusSign: getPart(minusParts, "minusSign", "-"),
    percentSign: getPart(percentParts, "percentSign", "%"),
    percentPattern: inferPercentPattern(percentParts),
    currencyPattern: inferCurrencyPattern(currencyParts),
    grouping,
    compactPatterns: inferCompactPatterns(locale),
  };
}

function inferCompactPatterns(locale) {
  return {
    short: inferCompactDisplayPatterns(locale, "short"),
    long: inferCompactDisplayPatterns(locale, "long"),
  };
}

function inferCompactDisplayPatterns(locale, compactDisplay) {
  const patterns = [];

  for (let power = 3; power <= 15; power += 1) {
    const one = getCompactPattern(locale, compactDisplay, power, 1);
    const other = getCompactPattern(locale, compactDisplay, power, 2);

    if (!one || !other || one.divisorPower !== other.divisorPower) {
      continue;
    }

    const candidate = {
      thresholdPower: power,
      divisorPower: one.divisorPower,
      one: affixesEqual(one.affix, other.affix) ? undefined : one.affix,
      other: other.affix,
    };

    if (!isDuplicateCompactPattern(patterns[patterns.length - 1], candidate)) {
      patterns.push(candidate);
    }
  }

  return patterns;
}

function isDuplicateCompactPattern(previous, next) {
  return (
    previous !== undefined &&
    previous.divisorPower === next.divisorPower &&
    optionalAffixesEqual(previous.one, next.one) &&
    affixesEqual(previous.other, next.other)
  );
}

function optionalAffixesEqual(left, right) {
  if (left === undefined || right === undefined) {
    return left === right;
  }

  return affixesEqual(left, right);
}

function getCompactPattern(locale, compactDisplay, power, multiplier) {
  const value = multiplier * 10 ** power;
  const parts = new Intl.NumberFormat(locale, {
    notation: "compact",
    compactDisplay,
    maximumFractionDigits: 0,
  }).formatToParts(value);
  const numericIndexes = parts
    .map((part, index) => (isNumericPart(part) ? index : -1))
    .filter((index) => index >= 0);
  const compactIndex = parts.findIndex((part) => part.type === "compact");

  if (numericIndexes.length === 0 || compactIndex < 0) {
    return null;
  }

  const firstNumericIndex = numericIndexes[0];
  const lastNumericIndex = numericIndexes[numericIndexes.length - 1];
  const numericDigits = parts
    .slice(firstNumericIndex, lastNumericIndex + 1)
    .filter((part) => part.type === "integer" || part.type === "fraction")
    .map((part) => part.value)
    .join("");

  if (!/^[1-9]\d*$/.test(numericDigits)) {
    return null;
  }

  return {
    divisorPower: power - (numericDigits.length - String(multiplier).length),
    affix: {
      prefix: parts
        .slice(0, firstNumericIndex)
        .map((part) => part.value)
        .join(""),
      suffix: parts
        .slice(lastNumericIndex + 1)
        .map((part) => part.value)
        .join(""),
    },
  };
}

function affixesEqual(left, right) {
  return left.prefix === right.prefix && left.suffix === right.suffix;
}

function inferPercentPattern(parts) {
  const numericIndexes = parts
    .map((part, index) => (isNumericPart(part) ? index : -1))
    .filter((index) => index >= 0);
  const firstNumericIndex = numericIndexes[0];
  const lastNumericIndex = numericIndexes[numericIndexes.length - 1];

  if (firstNumericIndex === undefined || lastNumericIndex === undefined) {
    return {
      prefix: "",
      suffix: getPart(parts, "percentSign", "%"),
    };
  }

  return {
    prefix: parts
      .slice(0, firstNumericIndex)
      .map((part) => part.value)
      .join(""),
    suffix: parts
      .slice(lastNumericIndex + 1)
      .map((part) => part.value)
      .join(""),
  };
}

function inferCurrencyPattern(parts) {
  const numericIndexes = parts
    .map((part, index) => (isNumericPart(part) ? index : -1))
    .filter((index) => index >= 0);
  const firstNumericIndex = numericIndexes[0];
  const lastNumericIndex = numericIndexes[numericIndexes.length - 1];

  if (firstNumericIndex === undefined || lastNumericIndex === undefined) {
    return {
      prefix: "{currency}",
      suffix: "",
    };
  }

  return {
    prefix: renderCurrencyAffix(parts.slice(0, firstNumericIndex)),
    suffix: renderCurrencyAffix(parts.slice(lastNumericIndex + 1)),
  };
}

function renderCurrencyAffix(parts) {
  return parts
    .map((part) => (part.type === "currency" ? "{currency}" : part.value))
    .join("");
}

function isNumericPart(part) {
  return (
    part.type === "integer" ||
    part.type === "group" ||
    part.type === "decimal" ||
    part.type === "fraction"
  );
}

function inferGrouping(locale) {
  const parts = new Intl.NumberFormat(locale).formatToParts(123456789);
  const group = getPart(parts, "group", ",");
  const integerGroups = parts
    .filter((part) => part.type === "integer")
    .map((part) => part.value);
  const sizes = integerGroups.map((part) => part.length);
  const primary = sizes[sizes.length - 1] ?? 3;
  const secondary = sizes.length > 2 ? sizes[sizes.length - 2] : primary;

  return {
    primary,
    secondary: secondary === primary ? undefined : secondary,
    separator: group,
  };
}

function getPart(parts, type, fallback) {
  return parts.find((part) => part.type === type)?.value ?? fallback;
}

main();
